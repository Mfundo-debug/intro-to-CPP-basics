#include <iostream>
#include <cmath>

using namespace std;
//program to find the sqrt of a number
int main()
{
    double number, squareRoot;
    number =25.0;
    //sqrt() is a library function
    squareRoot = sqrt(number);
    cout<< "Square root of"<<number<<" = "<<squareRoot;
    return 0;
}
